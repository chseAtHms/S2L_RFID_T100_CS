/*------------------------------------------------------------------------------
**
** timerSYS_wait()
**
** Description:
**    wait x us
**
**------------------------------------------------------------------------------
** Inputs:
**    waitTime: time to wait in us
**
** Register Setting:
**    - None -
**
** Return:
**    - void -
**
** Usage:
**    wait for a dedicated time, timerSYS_wait(x);
**
** Module Test: - YES -
**      Reason: -
**------------------------------------------------------------------------------
*/
void timerSYS_wait(UINT32 waitTime)
{
   UINT32 u32SysTime;
   UINT32 u32timeElapsed;

   u32SysTime = timerHAL_GetSystemTime3();
   u32timeElapsed = 0u;

   while ( waitTime >= u32timeElapsed )
   {
      u32timeElapsed = (timerHAL_GetSystemTime3() - u32SysTime);
   }
}


